def get_folder_name(folder_name, account_type):
    """Get the correct folder name for the account type."""
    if account_type == 'a2':
        return f"INBOX.{folder_name}"
    return folder_name


def ensure_folder_exists(imap, folder_name, account_type):
    """Create folder if it doesn't exist."""
    # Get the correct folder name for this account type
    full_folder_name = get_folder_name(folder_name, account_type)

    # Check if folder exists
    status, folders = imap.list()
    folder_exists = False

    for f in folders:
        if f:
            # Parse folder name from IMAP response
            try:
                decoded = f.decode()
                # Check if our folder name appears in the listing
                if full_folder_name.upper() in decoded.upper():
                    folder_exists = True
                    break
            except:
                pass

    if not folder_exists:
        result = imap.create(full_folder_name)
        if result[0] == 'OK':
            print(f"    Created folder: {full_folder_name}")
        else:
            print(f"    Warning: Could not create folder {full_folder_name}: {result}")

    return full_folder_name


def process_account(email_addr, config, dry_run=False, include_bounces=True, include_marketing=True, stats=None):
    """Process a single email account."""
    password = os.getenv(config['password_env'])
    if not password:
        logger.warning(f"No password found for {email_addr}")
        print(f"  [SKIP] No password found for {email_addr}")
        return {'autosvar': 0, 'bounces': 0, 'marketing': 0, 'moved': 0}

    try:
        # Connect to IMAP
        imap = imaplib.IMAP4_SSL(config['server'], config['port'])
        imap.login(email_addr, password)

        # Select INBOX
        status, msgs = imap.select('INBOX')
        if status != 'OK':
            print(f"  [ERROR] Could not select INBOX: {msgs}")
            return {'autosvar': 0, 'bounces': 0, 'marketing': 0, 'moved': 0}

        total_messages = int(msgs[0])
        print(f"  INBOX: {total_messages} messages")

        if total_messages == 0:
            imap.logout()
            return {'autosvar': 0, 'bounces': 0, 'marketing': 0, 'moved': 0}

        # Ensure target folders exist and get correct folder names
        # Marketing emails are deleted, not moved to folder
        if not dry_run:
            autosvar_folder = ensure_folder_exists(imap, TARGET_FOLDER_AUTOSVAR, config['type'])
            if include_bounces:
                bounces_folder = ensure_folder_exists(imap, TARGET_FOLDER_BOUNCES, config['type'])
            else:
                bounces_folder = None
        else:
            autosvar_folder = get_folder_name(TARGET_FOLDER_AUTOSVAR, config['type'])
            bounces_folder = get_folder_name(TARGET_FOLDER_BOUNCES, config['type']) if include_bounces else None

        # Re-select INBOX after folder operations
        imap.select('INBOX')

        # Search all messages
        status, message_ids = imap.search(None, 'ALL')
        if status != 'OK':
            print(f"  [ERROR] Search failed: {message_ids}")
            imap.logout()
            return {'autosvar': 0, 'bounces': 0, 'marketing': 0, 'moved': 0}

        ids = message_ids[0].split()
        autosvar_count = 0
        bounce_count = 0
        marketing_count = 0
        moved_count = 0

        for msg_id in ids:
            try:
                # Fetch headers only (more efficient)
                status, data = imap.fetch(msg_id, '(RFC822.HEADER)')
                if status != 'OK' or not data or not data[0]:
                    continue

                raw_header = data[0][1] if isinstance(data[0], tuple) and len(data[0]) > 1 else None
                if not raw_header:
                    continue

                msg = email.message_from_bytes(raw_header)
                subject = decode_subject(msg)[:50]
                from_addr = msg.get('From', '')[:40]
            except Exception as e:
                continue

            # Check for bounce first
            is_bounce_email, bounce_reason = is_bounce(msg)
            if is_bounce_email and include_bounces:
                bounce_count += 1
                # Log the detection
                logger.info(f"BOUNCE | {email_addr} | {from_addr} | {subject} | {bounce_reason}")
                # Update stats
                if stats is not None:
                    update_stats(stats, email_addr, bounce_reason, 'bounce')
                if dry_run:
                    print(f"    [BOUNCE] {subject}")
                    print(f"             From: {from_addr}")
                    print(f"             Reason: {bounce_reason}")
                else:
                    result = imap.copy(msg_id, bounces_folder)
                    if result[0] == 'OK':
                        imap.store(msg_id, '+FLAGS', '\\Deleted')
                        moved_count += 1
                        print(f"    [MOVED->BOUNCES] {subject}")
                    else:
                        logger.error(f"Could not move bounce: {result}")
                        print(f"    [ERROR] Could not move bounce: {result}")
                continue

            # Check for marketing - DELETE these
            is_mkt, mkt_reason = is_marketing(msg)
            if is_mkt and include_marketing:
                marketing_count += 1
                logger.info(f"MARKETING | {email_addr} | {from_addr} | {subject} | {mkt_reason}")
                if stats is not None:
                    update_stats(stats, email_addr, mkt_reason, 'marketing')
                if dry_run:
                    print(f"    [MARKETING] {subject}")
                    print(f"                From: {from_addr}")
                    print(f"                Reason: {mkt_reason}")
                else:
                    # Try to unsubscribe first
                    http_url, mailto_url = extract_unsubscribe_link(msg)
                    unsub_result = None
                    if http_url or mailto_url:
                        unsub_result = auto_unsubscribe(http_url, mailto_url, from_addr, subject)

                    # Save to blocked senders list
                    save_blocked_sender(from_addr, subject, mkt_reason)

                    # Delete marketing emails directly (no copy to folder)
                    imap.store(msg_id, '+FLAGS', '\\Deleted')
                    moved_count += 1
                    if unsub_result and unsub_result.startswith('SUCCESS'):
                        print(f"    [UNSUBSCRIBED+DELETED] {subject}")
                    else:
                        print(f"    [DELETED] {subject}")
                continue

            # Check for autosvar
            is_auto, auto_reason = is_autosvar(msg)
            if is_auto:
                autosvar_count += 1
                # Log the detection
                logger.info(f"AUTOSVAR | {email_addr} | {from_addr} | {subject} | {auto_reason}")
                # Update stats
                if stats is not None:
                    update_stats(stats, email_addr, auto_reason, 'autosvar')
                if dry_run:
                    print(f"    [AUTOSVAR] {subject}")
                    print(f"               From: {from_addr}")
                    print(f"               Reason: {auto_reason}")
                else:
                    result = imap.copy(msg_id, autosvar_folder)
                    if result[0] == 'OK':
                        imap.store(msg_id, '+FLAGS', '\\Deleted')
                        moved_count += 1
                        print(f"    [MOVED->AUTOMATED] {subject}")
                    else:
                        logger.error(f"Could not move autosvar: {result}")
                        print(f"    [ERROR] Could not move autosvar: {result}")

        # Expunge deleted messages
        if not dry_run and moved_count > 0:
            imap.expunge()

        imap.logout()
        return {'autosvar': autosvar_count, 'bounces': bounce_count, 'marketing': marketing_count, 'moved': moved_count}

    except imaplib.IMAP4.error as e:
        logger.error(f"IMAP error for {email_addr}: {e}")
        print(f"  [IMAP ERROR] {e}")
        return {'autosvar': 0, 'bounces': 0, 'marketing': 0, 'moved': 0}
    except Exception as e:
        logger.error(f"Error processing {email_addr}: {e}")
        print(f"  [ERROR] {e}")
        return {'autosvar': 0, 'bounces': 0, 'marketing': 0, 'moved': 0}


