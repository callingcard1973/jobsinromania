def save_stats(stats):
    """Save statistics to file."""
    # Convert defaultdict to regular dict for JSON serialization
    if isinstance(stats.get('by_reason'), defaultdict):
        stats['by_reason'] = dict(stats['by_reason'])
    stats['last_run'] = datetime.now().isoformat()
    with open(STATS_FILE, 'w') as f:
        json.dump(stats, f, indent=2)


def update_stats(stats, account, reason, email_type):
    """Update statistics with a detection."""
    today = datetime.now().strftime('%Y-%m-%d')

    # Initialize account stats if needed
    if account not in stats['by_account']:
        stats['by_account'][account] = {'autosvar': 0, 'bounces': 0, 'marketing': 0}

    # Ensure marketing key exists (for old stats files)
    if 'marketing' not in stats['by_account'][account]:
        stats['by_account'][account]['marketing'] = 0
    if 'total_marketing' not in stats:
        stats['total_marketing'] = 0

    # Update counters
    if email_type == 'autosvar':
        stats['total_autosvar'] += 1
        stats['by_account'][account]['autosvar'] += 1
    elif email_type == 'bounce':
        stats['total_bounces'] += 1
        stats['by_account'][account]['bounces'] += 1
    elif email_type == 'marketing':
        stats['total_marketing'] += 1
        stats['by_account'][account]['marketing'] += 1

    stats['total_processed'] += 1

    # Track detection reasons
    if reason:
        if isinstance(stats['by_reason'], dict):
            stats['by_reason'][reason] = stats['by_reason'].get(reason, 0) + 1
        else:
            stats['by_reason'][reason] += 1

    # Track daily stats
    if today not in stats['by_date']:
        stats['by_date'][today] = {'autosvar': 0, 'bounces': 0, 'marketing': 0}
    if 'marketing' not in stats['by_date'][today]:
        stats['by_date'][today]['marketing'] = 0

    if email_type == 'autosvar':
        stats['by_date'][today]['autosvar'] += 1
    elif email_type == 'bounce':
        stats['by_date'][today]['bounces'] += 1
    elif email_type == 'marketing':
        stats['by_date'][today]['marketing'] += 1


