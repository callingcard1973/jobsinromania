def main():
    parser = argparse.ArgumentParser(description='Filter autosvar, bounces, and marketing emails to separate folders')
    parser.add_argument('--account', choices=['gmail', 'a2', 'all'], default='all',
                        help='Which accounts to process')
    parser.add_argument('--dry-run', action='store_true',
                        help='Show what would be moved without moving')
    parser.add_argument('--status', action='store_true',
                        help='Show account status')
    parser.add_argument('--stats', action='store_true',
                        help='Show detection statistics')
    parser.add_argument('--email', type=str,
                        help='Process specific email address only')
    parser.add_argument('--no-bounces', action='store_true',
                        help='Skip bounce processing')
    parser.add_argument('--no-marketing', action='store_true',
                        help='Skip marketing email processing')
    args = parser.parse_args()

    if args.status:
        show_status()
        return

    if args.stats:
        show_stats()
        return

    # Load statistics for tracking
    stats = load_stats()

    logger.info(f"Starting autosvar filter - Mode: {'DRY RUN' if args.dry_run else 'LIVE'}")
    print("=== AUTOSVAR FILTER ===")
    print(f"Mode: {'DRY RUN' if args.dry_run else 'LIVE'}")
    print(f"Autosvar  -> {TARGET_FOLDER_AUTOSVAR}")
    if not args.no_bounces:
        print(f"Bounces   -> {TARGET_FOLDER_BOUNCES}")
    if not args.no_marketing:
        print(f"Marketing -> DELETE")
    print(f"Log file: {LOG_FILE}")
    print()

    total_autosvar = 0
    total_bounces = 0
    total_marketing = 0
    total_moved = 0

    for email_addr, config in ACCOUNTS.items():
        # Filter by account type
        if args.account != 'all' and config['type'] != args.account:
            continue

        # Filter by specific email
        if args.email and email_addr != args.email:
            continue

        print(f"Processing: {email_addr}")
        result = process_account(email_addr, config, args.dry_run, not args.no_bounces, not args.no_marketing, stats)
        total_autosvar += result['autosvar']
        total_bounces += result['bounces']
        total_marketing += result['marketing']
        total_moved += result['moved']

        found = result['autosvar'] + result['bounces'] + result['marketing']
        if found > 0:
            if result['autosvar'] > 0:
                print(f"  Autosvar: {result['autosvar']}")
            if result['bounces'] > 0:
                print(f"  Bounces: {result['bounces']}")
            if result['marketing'] > 0:
                print(f"  Marketing: {result['marketing']}")
            if not args.dry_run:
                print(f"  Total moved: {result['moved']}")
        else:
            print(f"  Nothing found")
        print()

    # Save statistics
    save_stats(stats)

    print("=== SUMMARY ===")
    print(f"Autosvar found: {total_autosvar}")
    print(f"Bounces found: {total_bounces}")
    print(f"Marketing found: {total_marketing}")
    print(f"Total: {total_autosvar + total_bounces + total_marketing}")
    if not args.dry_run:
        print(f"Total moved: {total_moved}")
    else:
        print("(Dry run - no emails moved)")

    logger.info(f"Completed - Autosvar: {total_autosvar}, Bounces: {total_bounces}, Marketing: {total_marketing}, Moved: {total_moved}")


if __name__ == '__main__':
    main()
