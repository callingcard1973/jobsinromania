def export_contacts(campaign_cfg, output_file, dry_run=False):
    """Export contacts from PostgreSQL to CSV."""
    import psycopg2

    db = campaign_cfg['db']
    tables = campaign_cfg.get('tables', {})
    contacts_table = tables.get('contacts', 'contacts')

    # Build query
    col_email = tables.get('col_email', 'email')
    col_company = tables.get('col_company', tables.get('col_name', 'company_name'))
    col_city = tables.get('col_city', 'city')

    query = f"""
        SELECT {col_email} as email, {col_company} as company, {col_city} as city
        FROM {contacts_table}
        WHERE {col_email} IS NOT NULL AND {col_email} != ''
    """

    # Add filter if present
    if 'filter' in campaign_cfg:
        query += f" AND ({campaign_cfg['filter']})"

    if dry_run:
        print(f"  [DRY RUN] Would export from {db['dbname']}.{contacts_table}")
        print(f"  Query: {query[:200]}...")
        return 0

    conn = psycopg2.connect(**db)
    cur = conn.cursor()
    cur.execute(query)
    rows = cur.fetchall()
    conn.close()

    with open(output_file, 'w', newline='') as f:
        writer = csv.writer(f)
        writer.writerow(['email', 'company', 'city'])
        for row in rows:
            writer.writerow(row)

    return len(rows)


def run_ssh(cmd, dry_run=False):
    """Run command on raspi via SSH."""
    full_cmd = f"ssh -o ConnectTimeout=5 {RASPI_HOST} '{cmd}'"
    if dry_run:
        print(f"  [DRY RUN] {full_cmd}")
        return True, ""
    result = subprocess.run(full_cmd, shell=True, capture_output=True, text=True)
    return result.returncode == 0, result.stdout + result.stderr


def rsync_to_raspi(local_path, remote_path, dry_run=False):
    """Rsync file to raspi."""
    cmd = f"rsync -avz {local_path} {RASPI_HOST}:{remote_path}"
    if dry_run:
        print(f"  [DRY RUN] {cmd}")
        return True
    result = subprocess.run(cmd, shell=True, capture_output=True, text=True)
    return result.returncode == 0


def migrate_campaign(campaign_name, campaign_cfg, dry_run=False):
    """Migrate a campaign from raspibig to raspi."""
    print(f"\n{'='*60}")
    print(f"MIGRATING: {campaign_name}")
    print(f"{'='*60}")

    # Derive names
    safe_name = campaign_name.lower().replace(' ', '_').replace('-', '_')
    raspi_dir = f"{RASPI_CAMPAIGNS_DIR}/{safe_name.upper()}"
    script_name = f"{safe_name}_sender.py"
    state_file_name = f"{safe_name}_sender"

    # Extract sender config from campaign
    # These would come from the unified config or sector config
    gmail_env_user = campaign_cfg.get('gmail_env_user', 'GMAIL_USER')
    gmail_env_pass = campaign_cfg.get('gmail_env_pass', 'GMAIL_APP_PASSWORD')
    gmail_limit = campaign_cfg.get('gmail_limit', 100)

    brevo_env_key = campaign_cfg.get('brevo_env_key', 'BREVO_API_KEY')
    brevo_sender = campaign_cfg.get('brevo_sender', 'office@example.com')
    brevo_name = campaign_cfg.get('brevo_name', 'Sender Name')
    brevo_limit = campaign_cfg.get('brevo_limit', 290)

    delay_min = campaign_cfg.get('delay_min', 180)
    delay_max = campaign_cfg.get('delay_max', 240)

    bh_start = campaign_cfg.get('business_hours_start', 8)
    bh_end = campaign_cfg.get('business_hours_end', 18)
    bh_days = campaign_cfg.get('business_days', [0, 1, 2, 3, 4])

    # Step 1: Create directories on raspi
    print("\n[1/7] Creating directories on raspi...")
    run_ssh(f"mkdir -p {raspi_dir}/{{contacts,templates,logs}}", dry_run)

    # Step 2: Export contacts
    print("\n[2/7] Exporting contacts from PostgreSQL...")
    tmp_csv = f"/tmp/{safe_name}_contacts.csv"
    count = export_contacts(campaign_cfg, tmp_csv, dry_run)
    print(f"  Exported {count} contacts")

    # Step 3: Copy contacts to raspi
    print("\n[3/7] Copying contacts to raspi...")
    rsync_to_raspi(tmp_csv, f"{raspi_dir}/contacts/contacts.csv", dry_run)

    # Step 4: Copy template
    print("\n[4/7] Copying template...")
    template_dir = campaign_cfg.get('templates_dir', f"{UNIFIED_DIR}/templates/{safe_name}/")
    template_file = None
    if os.path.isdir(template_dir):
        for tpl in Path(template_dir).glob("*.txt"):
            template_file = str(tpl)
            break

    if template_file:
        rsync_to_raspi(template_file, f"{raspi_dir}/templates/template.txt", dry_run)
    else:
        print(f"  WARNING: No template found in {template_dir}")

    # Step 5: Generate sender script
    print("\n[5/7] Generating sender script...")
    script_content = SENDER_SCRIPT_TEMPLATE.format(
        campaign_name=campaign_name.upper(),
        date=datetime.now().strftime('%Y-%m-%d'),
        script_name=script_name,
        state_file_name=state_file_name,
        gmail_env_user=gmail_env_user,
        gmail_env_pass=gmail_env_pass,
        gmail_limit=gmail_limit,
        brevo_env_key=brevo_env_key,
        brevo_sender=brevo_sender,
        brevo_name=brevo_name,
        brevo_limit=brevo_limit,
        delay_min=delay_min,
        delay_max=delay_max,
        bh_start=bh_start,
        bh_end=bh_end,
        bh_days=str(bh_days)
    )

    tmp_script = f"/tmp/{script_name}"
    if not dry_run:
        with open(tmp_script, 'w') as f:
            f.write(script_content)
    rsync_to_raspi(tmp_script, f"{raspi_dir}/{script_name}", dry_run)
    run_ssh(f"chmod +x {raspi_dir}/{script_name}", dry_run)

    # Step 6: Create wrapper script
    print("\n[6/7] Creating wrapper script...")
    wrapper = f"""#!/bin/bash
cd {raspi_dir}
/opt/venv/bin/python3 {script_name} "$@"
"""
    tmp_wrapper = f"/tmp/run-{safe_name}.sh"
    if not dry_run:
        with open(tmp_wrapper, 'w') as f:
            f.write(wrapper)
    rsync_to_raspi(tmp_wrapper, f"{raspi_dir}/run-{safe_name}.sh", dry_run)
    run_ssh(f"chmod +x {raspi_dir}/run-{safe_name}.sh", dry_run)

    # Step 7: Update pipeline.json
    print("\n[7/7] Updating pipeline.json...")
    if not dry_run:
        config = load_pipeline_config()

        # Mark original campaign as migrated
        if campaign_name in config.get('campaigns', {}):
            config['campaigns'][campaign_name]['status'] = 'MIGRATED_TO_RASPI'
            config['campaigns'][campaign_name]['migrated_date'] = datetime.now().strftime('%Y-%m-%d')
            config['campaigns'][campaign_name]['raspi_path'] = raspi_dir

        # Add to raspi_campaigns
        if 'raspi_campaigns' not in config:
            config['raspi_campaigns'] = {}

        config['raspi_campaigns'][safe_name] = {
            'host': RASPI_HOST,
            'path': f"{raspi_dir}/",
            'script': script_name,
            'status_file': 'status.json',
            'state_file': f'.{state_file_name}_state.json',
            'business_hours': f"{bh_start}:00-{bh_end}:00 Mon-Fri",
            'senders': {
                'gmail': {'email': gmail_env_user, 'limit': gmail_limit},
                'brevo': {'email': brevo_sender, 'limit': brevo_limit}
            },
            'cron': '40 8,12,16 * * *',
            'daily_capacity': gmail_limit + brevo_limit,
            'migrated_from': 'raspibig unified-orchestrator',
            'migrated_date': datetime.now().strftime('%Y-%m-%d')
        }

        save_pipeline_config(config)

    print(f"\n{'='*60}")
    print(f"MIGRATION COMPLETE: {campaign_name}")
    print(f"{'='*60}")
    print(f"\nRaspi location: {raspi_dir}")
    print(f"\nNext steps:")
    print(f"  1. Add credentials to raspi /opt/EMAIL/.env:")
    print(f"     {gmail_env_user}=...")
    print(f"     {gmail_env_pass}=...")
    print(f"     {brevo_env_key}=...")
    print(f"\n  2. Test on raspi:")
    print(f"     ssh raspi '{raspi_dir}/run-{safe_name}.sh --test --limit 3'")
    print(f"\n  3. Add cron job on raspi:")
    print(f"     40 8,12,16 * * * {raspi_dir}/run-{safe_name}.sh --limit 100")
    print(f"\n  4. Restart dashboard:")
    print(f"     sudo systemctl restart pipeline-dashboard")


def main():
    parser = argparse.ArgumentParser(description='Migrate campaign from raspibig to raspi')
    parser.add_argument('--list', action='store_true', help='List migratable campaigns')
    parser.add_argument('--campaign', help='Campaign name to migrate')
    parser.add_argument('--config', help='Campaign config JSON file')
    parser.add_argument('--dry-run', action='store_true', help='Show what would be done')
    parser.add_argument('--execute', action='store_true', help='Actually perform migration')
    args = parser.parse_args()

    if args.list:
        campaigns = get_migratable_campaigns()
        print("Migratable campaigns:")
        print("-" * 60)
        for c in campaigns:
            print(f"  {c['name']}: {c['db']}.{c['table']} ({c['daily_capacity']}/day)")
        return

    if args.campaign:
        config = load_pipeline_config()
        if args.campaign not in config.get('campaigns', {}):
            print(f"Campaign not found: {args.campaign}")
            print("Use --list to see available campaigns")
            sys.exit(1)

        campaign_cfg = config['campaigns'][args.campaign]
        if campaign_cfg.get('status') == 'MIGRATED_TO_RASPI':
            print(f"Campaign {args.campaign} is already migrated")
            sys.exit(1)

        if not args.execute and not args.dry_run:
            print("Use --dry-run to preview or --execute to perform migration")
            sys.exit(1)

        migrate_campaign(args.campaign, campaign_cfg, dry_run=args.dry_run)
        return

    if args.config:
        with open(args.config) as f:
            campaign_cfg = json.load(f)
        name = Path(args.config).stem
        migrate_campaign(name, campaign_cfg, dry_run=args.dry_run)
        return

    parser.print_help()


if __name__ == '__main__':
    main()
