def load_state():
    if STATE_FILE.exists():
        with open(STATE_FILE) as f:
            return json.load(f)
    return {{"last_date": None, "gmail_today": 0, "brevo_today": 0, "total_sent": 0}}


def save_state(state):
    with open(STATE_FILE, 'w') as f:
        json.dump(state, f, indent=2)


def load_sent_log():
    if SENT_LOG.exists():
        with open(SENT_LOG) as f:
            return set(json.load(f))
    return set()


def save_sent_log(sent):
    with open(SENT_LOG, 'w') as f:
        json.dump(list(sent), f)


def load_dnc():
    dnc = set()
    if DNC_FILE.exists():
        with open(DNC_FILE) as f:
            for line in f:
                email = line.strip().lower()
                if email and '@' in email:
                    dnc.add(email)
    return dnc


def is_business_hours():
    now = datetime.now()
    if now.weekday() not in BUSINESS_DAYS:
        return False, f"Weekend (day {{now.weekday()}})"
    if now.hour < BUSINESS_HOURS_START:
        return False, f"Before {{BUSINESS_HOURS_START}}:00"
    if now.hour >= BUSINESS_HOURS_END:
        return False, f"After {{BUSINESS_HOURS_END}}:00"
    return True, "OK"


def load_contacts():
    contacts = []
    for csv_file in CONTACTS_DIR.glob("*.csv"):
        with open(csv_file, newline='', encoding='utf-8', errors='ignore') as f:
            reader = csv.DictReader(f)
            for row in reader:
                email = row.get('email', '').strip().lower()
                if email and '@' in email:
                    contacts.append({{
                        'email': email,
                        'company': row.get('company', row.get('company_name', '')),
                        'name': row.get('name', row.get('contact_name', '')),
                        'city': row.get('city', ''),
                        'source_file': csv_file.name
                    }})
    return contacts


def load_template():
    for tpl in TEMPLATES_DIR.glob("*.txt"):
        with open(tpl) as f:
            content = f.read()
        lines = content.split('\\n')
        subject = lines[0].replace('Subject:', '').strip() if lines[0].startswith('Subject:') else "Message"
        body = '\\n'.join(lines[2:]) if len(lines) > 2 else content
        return subject, body
    return "Message", "Hello"


def send_gmail(to_email, subject, body, test=False):
    if test:
        return True, "TEST"
    try:
        msg = MIMEMultipart()
        msg['From'] = f"{{BREVO_NAME}} <{{GMAIL_USER}}>"
        msg['To'] = to_email
        msg['Subject'] = subject
        msg.attach(MIMEText(body, 'plain', 'utf-8'))

        with smtplib.SMTP_SSL('smtp.gmail.com', 465, timeout=30) as server:
            server.login(GMAIL_USER, GMAIL_PASS)
            server.send_message(msg)
        return True, "GMAIL"
    except Exception as e:
        return False, str(e)[:50]


def send_brevo(to_email, subject, body, test=False):
    if test:
        return True, "TEST"
    try:
        import requests
        resp = requests.post(
            'https://api.brevo.com/v3/smtp/email',
            headers={{'api-key': BREVO_API_KEY, 'Content-Type': 'application/json'}},
            json={{
                'sender': {{'email': BREVO_SENDER, 'name': BREVO_NAME}},
                'to': [{{'email': to_email}}],
                'subject': subject,
                'textContent': body
            }},
            timeout=30
        )
        if resp.status_code in (200, 201):
            return True, "BREVO"
        return False, f"HTTP {{resp.status_code}}"
    except Exception as e:
        return False, str(e)[:50]


def get_status_dict(state, contacts, sent_log, dnc):
    bh_ok, bh_reason = is_business_hours()
    contact_files = []
    for csv_file in CONTACTS_DIR.glob("*.csv"):
        with open(csv_file) as f:
            total = sum(1 for _ in f) - 1
        contact_files.append({{'file': csv_file.name, 'total': total, 'remaining': total - len(sent_log)}})

    return {{
        'campaign': CAMPAIGN_NAME,
        'location': 'raspi',
        'status': 'active' if bh_ok else 'outside_hours',
        'business_hours': f"{{BUSINESS_HOURS_START}}:00-{{BUSINESS_HOURS_END}}:00 Mon-Fri",
        'is_business_hours': bh_ok,
        'business_hours_reason': bh_reason,
        'last_date': state.get('last_date'),
        'gmail_today': state.get('gmail_today', 0),
        'gmail_limit': GMAIL_LIMIT,
        'brevo_today': state.get('brevo_today', 0),
        'brevo_limit': BREVO_LIMIT,
        'total_sent': state.get('total_sent', 0),
        'sent_log_count': len(sent_log),
        'dnc_count': len(dnc),
        'contacts': contact_files,
        'remaining': sum(c['remaining'] for c in contact_files),
        'updated': datetime.now().isoformat()
    }}


def export_status(state, contacts, sent_log, dnc):
    status = get_status_dict(state, contacts, sent_log, dnc)
    with open(STATUS_FILE, 'w') as f:
        json.dump(status, f, indent=2)


