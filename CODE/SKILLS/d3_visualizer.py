def read_csv_data(filepath: str) -> tuple:
    """Read CSV and return headers and rows"""
    with open(filepath, 'r', encoding='utf-8', errors='ignore') as f:
        reader = csv.DictReader(f)
        headers = reader.fieldnames or []
        rows = list(reader)
    return headers, rows

def detect_chart_type(headers: List[str], rows: List[Dict]) -> str:
    """Auto-detect best chart type"""
    if not rows:
        return 'bar'

    # Check for date columns (line chart)
    for h in headers:
        if 'date' in h.lower() or 'time' in h.lower():
            return 'line'

    # Check for categorical data with few unique values (pie chart)
    for h in headers:
        values = [r.get(h, '') for r in rows[:100]]
        unique = len(set(values))
        if 3 <= unique <= 10:
            return 'pie'

    return 'bar'

def prepare_chart_data(rows: List[Dict], label_col: str, value_col: str = None) -> List[Dict]:
    """Prepare data for charting"""
    data = []

    if value_col:
        # Use specified value column
        for row in rows[:50]:
            label = str(row.get(label_col, ''))[:30]
            try:
                value = float(str(row.get(value_col, 0)).replace(',', ''))
            except Exception:
                value = 0
            if label and value:
                data.append({'label': label, 'value': value})
    else:
        # Count occurrences
        counter = Counter()
        for row in rows:
            label = str(row.get(label_col, ''))[:30]
            if label:
                counter[label] += 1
        data = [{'label': k, 'value': v} for k, v in counter.most_common(20)]

    return data

def find_columns(headers: List[str]) -> tuple:
    """Find label and value columns"""
    label_col = value_col = None

    # Find label column
    for h in headers:
        hl = h.lower()
        if any(x in hl for x in ['name', 'label', 'category', 'country', 'type', 'domain', 'email']):
            label_col = h
            break
    if not label_col:
        label_col = headers[0]

    # Find value column
    for h in headers:
        hl = h.lower()
        if any(x in hl for x in ['count', 'value', 'amount', 'total', 'score', 'num']):
            value_col = h
            break

    return label_col, value_col

# ============================================================
# CHART GENERATION
# ============================================================

def generate_chart(
    data: List[Dict],
    chart_type: str,
    title: str,
    data_source: str,
    output_path: str,
):
    """Generate HTML chart"""
    scripts = {
        'bar': BAR_CHART_SCRIPT,
        'pie': PIE_CHART_SCRIPT,
        'line': LINE_CHART_SCRIPT,
    }

    html = HTML_TEMPLATE.format(
        title=title,
        data_source=data_source,
        timestamp=datetime.now().strftime('%Y-%m-%d %H:%M'),
        data_json=json.dumps(data),
        chart_script=scripts.get(chart_type, BAR_CHART_SCRIPT),
    )

    with open(output_path, 'w', encoding='utf-8') as f:
        f.write(html)

    return output_path

