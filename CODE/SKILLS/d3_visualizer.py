def main():
    args = sys.argv[1:]

    if not args or '-h' in args or '--help' in args:
        print(f"""
{'='*60}
D3 VISUALIZER
{'='*60}

Usage: d3_visualizer.py <input.csv> [options]

Options:
  --type TYPE       Chart type: bar, pie, line (default: auto)
  --output FILE     Output HTML file (default: chart.html)
  --title TEXT      Chart title
  --label COL       Column to use for labels
  --value COL       Column to use for values (if numeric)

Examples:
  d3_visualizer.py contacts.csv --type pie
  d3_visualizer.py sales.csv --type bar --title "Sales by Region"
  d3_visualizer.py trends.csv --type line --output trends.html
  d3_visualizer.py data.csv --label country --value count
""")
