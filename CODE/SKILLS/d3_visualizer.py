BAR_CHART_SCRIPT = '''
const margin = {top: 30, right: 30, bottom: 100, left: 60};
const width = 800 - margin.left - margin.right;
const height = 400 - margin.top - margin.bottom;

const svg = d3.select("#chart")
    .append("svg")
    .attr("width", width + margin.left + margin.right)
    .attr("height", height + margin.top + margin.bottom)
    .append("g")
    .attr("transform", `translate(${margin.left},${margin.top})`);

const x = d3.scaleBand()
    .range([0, width])
    .domain(data.map(d => d.label))
    .padding(0.2);

const y = d3.scaleLinear()
    .domain([0, d3.max(data, d => d.value) * 1.1])
    .range([height, 0]);

svg.append("g")
    .attr("transform", `translate(0,${height})`)
    .call(d3.axisBottom(x))
    .selectAll("text")
    .attr("transform", "rotate(-45)")
    .style("text-anchor", "end");

svg.append("g")
    .call(d3.axisLeft(y));

const tooltip = d3.select("body").append("div").attr("class", "tooltip").style("opacity", 0);

svg.selectAll(".bar")
    .data(data)
    .enter()
    .append("rect")
    .attr("class", "bar")
    .attr("x", d => x(d.label))
    .attr("y", d => y(d.value))
    .attr("width", x.bandwidth())
    .attr("height", d => height - y(d.value))
    .on("mouseover", function(event, d) {
        tooltip.transition().duration(200).style("opacity", .9);
        tooltip.html(`${d.label}: ${d.value}`)
            .style("left", (event.pageX + 10) + "px")
            .style("top", (event.pageY - 28) + "px");
    })
    .on("mouseout", function(d) {
        tooltip.transition().duration(500).style("opacity", 0);
    });
'''

PIE_CHART_SCRIPT = '''
const width = 500, height = 400, radius = Math.min(width, height) / 2 - 40;

const color = d3.scaleOrdinal()
    .domain(data.map(d => d.label))
    .range(d3.schemeCategory10);

const svg = d3.select("#chart")
    .append("svg")
    .attr("width", width)
    .attr("height", height)
    .append("g")
    .attr("transform", `translate(${width/2},${height/2})`);

const pie = d3.pie().value(d => d.value);
const arc = d3.arc().innerRadius(0).outerRadius(radius);
const labelArc = d3.arc().innerRadius(radius * 0.6).outerRadius(radius * 0.6);

const tooltip = d3.select("body").append("div").attr("class", "tooltip").style("opacity", 0);

svg.selectAll("path")
    .data(pie(data))
    .enter()
    .append("path")
    .attr("class", "pie-slice")
    .attr("d", arc)
    .attr("fill", d => color(d.data.label))
    .on("mouseover", function(event, d) {
        tooltip.transition().duration(200).style("opacity", .9);
        const pct = (d.data.value / d3.sum(data, x => x.value) * 100).toFixed(1);
        tooltip.html(`${d.data.label}: ${d.data.value} (${pct}%)`)
            .style("left", (event.pageX + 10) + "px")
            .style("top", (event.pageY - 28) + "px");
    })
    .on("mouseout", function(d) {
        tooltip.transition().duration(500).style("opacity", 0);
    });

// Legend
const legend = d3.select("#legend").append("svg").attr("width", 300).attr("height", data.length * 25);
const legendItems = legend.selectAll(".legend-item").data(data).enter().append("g")
    .attr("transform", (d, i) => `translate(0, ${i * 22})`);
legendItems.append("rect").attr("width", 18).attr("height", 18).attr("fill", d => color(d.label));
legendItems.append("text").attr("x", 24).attr("y", 14).text(d => `${d.label}: ${d.value}`);
'''

