def load_cpanel_token():
    global CPANEL_TOKEN
    token = os.environ.get("A2_CPANEL_TOKEN")
    if token:
        CPANEL_TOKEN = token
        return True
    for path in [
        os.path.expanduser("~/.a2hosting.env"),
        "D:/MEMORY/.a2hosting.env",
        "D:/MEMORY/A2_SITE_DEPLOYER/.env",
    ]:
        try:
            with open(path) as f:
                for line in f:
                    line = line.strip()
                    if line.startswith("A2_CPANEL_TOKEN="):
                        CPANEL_TOKEN = line.split("=", 1)[1].strip().strip("\"'")
                        return True
        except FileNotFoundError:
            continue
    return False


def cpanel_api(module, function, params=None):
    if not CPANEL_TOKEN:
        return {"status": 0, "errors": ["No cPanel token"]}
    url = f"https://{CPANEL_HOST}:{CPANEL_PORT}/execute/{module}/{function}"
    if params:
        url += "?" + urllib.parse.urlencode(params)
    req = urllib.request.Request(url)
    req.add_header("Authorization", f"cpanel {CPANEL_USER}:{CPANEL_TOKEN}")
    try:
        with urllib.request.urlopen(req, context=make_ssl_context(), timeout=30) as resp:
            return json.loads(resp.read().decode())
    except urllib.error.HTTPError as e:
        return {"status": 0, "errors": [f"HTTP {e.code}: {e.reason}"]}
    except Exception as e:
        return {"status": 0, "errors": [str(e)]}


def setup_cpanel_autoresponders(dry_run=False):
    """Set up cPanel autoresponders on all A2 domains."""
    print("\n--- cPanel Autoresponders (A2 Hosting) ---")
