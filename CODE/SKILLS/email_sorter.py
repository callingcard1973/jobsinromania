def get_all_accounts():
    """Build unified account list with connection details."""
    accounts = []
    seen = set()

    for acc in A2_ACCOUNTS + GMAIL_ACCOUNTS + YAHOO_ACCOUNTS:
        addr = acc["email"]
        if addr in seen:
            continue
        seen.add(addr)

        pw = acc.get("password")
        if not pw:
            log_error(f"  SKIP {addr}: no password in config")
            continue

        domain = acc.get("domain", "")
        if "gmail.com" in addr:
            accounts.append({
                "email": addr, "password": pw,
                "imap_server": "imap.gmail.com", "imap_port": 993,
                "smtp_server": "smtp.gmail.com", "smtp_port": 587,
                "provider": "gmail", "domain": None,
            })
        elif "yahoo.com" in addr:
            accounts.append({
                "email": addr, "password": pw,
                "imap_server": "imap.mail.yahoo.com", "imap_port": 993,
                "smtp_server": "smtp.mail.yahoo.com", "smtp_port": 587,
                "provider": "yahoo", "domain": None,
            })
        elif domain:
            accounts.append({
                "email": addr, "password": pw,
                "imap_server": f"mail.{domain}", "imap_port": 993,
                "smtp_server": None, "smtp_port": None,
                "provider": "a2", "domain": domain,
            })

    return accounts


def process_account(acc, days=7, dry_run=False, sort=True, reply=True):
    """Process a single email account: create folders, sort, send replies."""
    email_addr = acc["email"]
    provider = acc["provider"]
    print(f"\n  [{provider.upper()}] {email_addr}")

    imap, err = imap_connect(acc["imap_server"], acc["imap_port"], email_addr, acc["password"])
    if err:
        print(f"    FAILED: {err}")
        return False

    try:
        # Create folders
        created = imap_ensure_folders(imap, provider)
        if created:
            print(f"    Created folders: {', '.join(created)}")

        # Sort emails
        if sort:
            stats = sort_account(imap, email_addr, provider, days, dry_run)
            print(f"    Sorted: {stats['autoreply']} autoreplies, {stats['application']} applications, "
                  f"{stats['skipped']} unchanged, {stats['errors']} errors")

        # Send SMTP auto-replies (Gmail + Yahoo only)
        if reply and acc["smtp_server"]:
            reply_stats = send_autoreplies_for_account(
                imap, email_addr, acc["password"],
                acc["smtp_server"], acc["smtp_port"],
                days, dry_run
            )
            if reply_stats["sent"] > 0 or reply_stats["errors"] > 0:
                print(f"    Replies: {reply_stats['sent']} sent, {reply_stats['skipped']} skipped, "
                      f"{reply_stats['errors']} errors")

        return True

    except Exception as e:
        print(f"    Error: {e}")
        return False

    finally:
        try:
            imap.logout()
        except:
            pass


