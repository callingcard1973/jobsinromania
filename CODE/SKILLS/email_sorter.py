def decode_header_value(raw):
    """Decode an email header value to string."""
    if raw is None:
        return ""
    parts = email.header.decode_header(raw)
    decoded = []
    for part, charset in parts:
        if isinstance(part, bytes):
            decoded.append(part.decode(charset or "utf-8", errors="replace"))
        else:
            decoded.append(str(part))
    return " ".join(decoded)


def get_sender_email(msg):
    """Extract sender email address from From header."""
    from_header = decode_header_value(msg.get("From", ""))
    # Try to extract email from "Name <email>" or just "email"
    match = re.search(r'<([^>]+)>', from_header)
    if match:
        return match.group(1).lower().strip()
    # Fallback: might just be the email
    if "@" in from_header:
        return from_header.strip().lower()
    return from_header.lower().strip()


def is_autoreply(msg):
    """Check if message is an auto-reply, out-of-office, or bounce."""
    # Check auto-reply headers
    for header in AUTOREPLY_HEADERS:
        if msg.get(header):
            return True

    # Check Auto-Submitted (RFC 3834)
    auto_submitted = (msg.get("Auto-Submitted") or "").lower().strip()
    if auto_submitted and auto_submitted != "no":
        return True

    # Check Precedence header
    precedence = (msg.get("Precedence") or "").lower().strip()
    if precedence in ("auto_reply", "autoreply", "auto-reply", "bulk", "junk"):
        return True

    # Check sender patterns
    sender = get_sender_email(msg)
    for pattern in AUTOREPLY_SENDER_PATTERNS:
        if pattern in sender:
            return True

    # Check if sender is bounce address
    sender_local = sender.split("@")[0] if "@" in sender else sender
    if sender_local in BOUNCE_SENDERS:
        return True

    # Check subject
    subject = decode_header_value(msg.get("Subject", ""))
    if AUTOREPLY_SUBJECT_RE.search(subject):
        return True
    if BOUNCE_SUBJECT_RE.search(subject):
        return True

    return False


def is_application(msg, body_text=""):
    """Check if message is a job application."""
    sender = get_sender_email(msg)
    subject = decode_header_value(msg.get("Subject", ""))

    # Check sender domain
    if "@" in sender:
        domain = sender.split("@")[1]
        if domain in APPLICATION_SENDER_DOMAINS:
            return True

    # Check subject
    if APPLICATION_SUBJECT_RE.search(subject):
        return True

    # Check body keywords
    if body_text and APPLICATION_BODY_RE.search(body_text):
        return True

    # Check attachment filenames
    content_type = msg.get("Content-Type", "")
    content_disp = msg.get("Content-Disposition", "")
    combined = f"{content_type} {content_disp}".lower()

    if APPLICATION_ATTACHMENT_RE.search(combined):
        return True

    for ext in APPLICATION_ATTACHMENT_EXTS:
        if ext in combined:
            return True

    return False


def classify_email(msg, body_text=""):
    """Classify email. Returns 'autoreply', 'application', or None."""
    if is_autoreply(msg):
        return "autoreply"
    if is_application(msg, body_text):
        return "application"
    return None


