def main():
    parser = argparse.ArgumentParser(description="Email sorter & autoresponder for all accounts")
    parser.add_argument("--sort-only", action="store_true", help="Only sort emails (no autoresponder setup)")
    parser.add_argument("--setup-only", action="store_true", help="Only create folders + autoresponders")
    parser.add_argument("--status", action="store_true", help="Show current state of all accounts")
    parser.add_argument("--scan", action="store_true", help="List every email in INBOX with classification (read-only)")
    parser.add_argument("--dry-run", action="store_true", help="Preview changes without executing")
    parser.add_argument("--days", type=int, default=7, help="Process emails from last N days (default: 7)")
    parser.add_argument("--account", type=str, help="Process single account (email address)")
    parser.add_argument("--no-reply", action="store_true", help="Skip SMTP auto-replies")
    parser.add_argument("--gmail-only", action="store_true", help="Only process Gmail accounts")
    parser.add_argument("--yahoo-only", action="store_true", help="Only process Yahoo accounts")
    parser.add_argument("--a2-only", action="store_true", help="Only process A2 Hosting accounts")
    args = parser.parse_args()

    if args.status:
        run_status()
        return

    accounts = get_all_accounts()

    # Filter by provider
    if args.gmail_only:
        accounts = [a for a in accounts if a["provider"] == "gmail"]
    elif args.yahoo_only:
        accounts = [a for a in accounts if a["provider"] == "yahoo"]
    elif args.a2_only:
        accounts = [a for a in accounts if a["provider"] == "a2"]

    # Filter to single account if specified
    if args.account:
        accounts = [a for a in accounts if a["email"] == args.account]
        if not accounts:
            print(f"Account not found: {args.account}")
            sys.exit(1)

    if args.scan:
        run_scan(accounts, args.days)
        return

    do_sort = not args.setup_only
    do_reply = not args.no_reply and not args.setup_only
    do_autoresponders = not args.sort_only

    mode = "DRY RUN" if args.dry_run else "LIVE"
    print("=" * 70)
    print(f"EMAIL SORTER ({mode}) — {len(accounts)} accounts, last {args.days} days")
    print("=" * 70)

    results = {"ok": 0, "fail": 0}

    for acc in accounts:
        ok = process_account(acc, args.days, args.dry_run, sort=do_sort, reply=do_reply)
        results["ok" if ok else "fail"] += 1
        time.sleep(0.3)

    print(f"\n--- RESULTS ---")
    print(f"Success: {results['ok']} | Failed: {results['fail']} | Total: {results['ok'] + results['fail']}")

    # Set up cPanel autoresponders
    if do_autoresponders:
        setup_cpanel_autoresponders(args.dry_run)

    print("\nDone.")


if __name__ == "__main__":
    main()
