def get_body_preview(msg_data):
    """Extract first ~2000 chars of body text from IMAP FETCH result."""
    try:
        msg = email.message_from_bytes(msg_data)
        if msg.is_multipart():
            for part in msg.walk():
                ct = part.get_content_type()
                if ct == "text/plain":
                    payload = part.get_payload(decode=True)
                    if payload:
                        return payload.decode(errors="replace")[:2000]
                elif ct == "text/html":
                    payload = part.get_payload(decode=True)
                    if payload:
                        text = re.sub(r'<[^>]+>', ' ', payload.decode(errors="replace"))
                        return text[:2000]
        else:
            payload = msg.get_payload(decode=True)
            if payload:
                return payload.decode(errors="replace")[:2000]
    except:
        pass
    return ""


def get_attachment_info(msg_data):
    """Get attachment filenames from BODYSTRUCTURE or full message."""
    filenames = []
    try:
        msg = email.message_from_bytes(msg_data)
        if msg.is_multipart():
            for part in msg.walk():
                fn = part.get_filename()
                if fn:
                    filenames.append(decode_header_value(fn).lower())
    except:
        pass
    return filenames


def sort_account(imap, account_email, provider="a2", days=7, dry_run=False):
    """Sort emails in one account's INBOX. Returns stats dict."""
    stats = {"autoreply": 0, "application": 0, "skipped": 0, "errors": 0}

    # Get actual folder names
    autoreply_folder = imap_get_folder_name(imap, "AUTOREPLY", provider)
    applications_folder = imap_get_folder_name(imap, "APPLICATIONS_RECEIVED", provider)

    # Select INBOX
    try:
        status, data = imap.select("INBOX")
        if status != "OK":
            print(f"    Cannot select INBOX: {data}")
            return stats
    except Exception as e:
        print(f"    Cannot select INBOX: {e}")
        return stats

    # Search for emails within date range
    since_date = (datetime.now() - timedelta(days=days)).strftime("%d-%b-%Y")
    try:
        status, data = imap.uid("SEARCH", None, f'(SINCE "{since_date}")')
        if status != "OK" or not data[0]:
            print(f"    No emails found in last {days} days")
            return stats
    except Exception as e:
        print(f"    Search error: {e}")
        return stats

    uids = data[0].split()
    print(f"    Found {len(uids)} emails in last {days} days")

    for uid in uids:
        uid_str = uid.decode() if isinstance(uid, bytes) else str(uid)

        try:
            # Fetch headers first (efficient)
            status, header_data = imap.uid("FETCH", uid, "(BODY.PEEK[HEADER])")
            if status != "OK" or not header_data or not header_data[0]:
                stats["errors"] += 1
                continue

            raw_headers = header_data[0][1] if isinstance(header_data[0], tuple) else header_data[0]
            if isinstance(raw_headers, bytes):
                msg = email.message_from_bytes(raw_headers)
            else:
                stats["errors"] += 1
                continue

            sender = get_sender_email(msg)
            subject = decode_header_value(msg.get("Subject", ""))[:80]

            # Check if autoreply first (headers only)
            if is_autoreply(msg):
                classification = "autoreply"
            else:
                # Need body for application detection
                body_text = ""
                attachment_names = []

                # Fetch body structure to check attachments
                try:
                    status2, body_data = imap.uid("FETCH", uid, "(BODY.PEEK[]<0.8000>)")
                    if status2 == "OK" and body_data and body_data[0]:
                        raw_body = body_data[0][1] if isinstance(body_data[0], tuple) else body_data[0]
                        if isinstance(raw_body, bytes):
                            body_text = get_body_preview(raw_body)
                            attachment_names = get_attachment_info(raw_body)
                except:
                    pass

                # Build a fake content header with attachment info for is_application
                if attachment_names:
                    for fn in attachment_names:
                        msg["X-Attachment-Names"] = fn

                if is_application(msg, body_text):
                    classification = "application"
                else:
                    classification = None

            if classification is None:
                stats["skipped"] += 1
                continue

            dest = autoreply_folder if classification == "autoreply" else applications_folder
            label = "AUTOREPLY" if classification == "autoreply" else "APPLICATION"

            if dry_run:
                print(f"    [DRY] {label}: {sender} | {subject}")
                stats[classification] += 1
            else:
                if imap_move_email(imap, uid, dest):
                    print(f"    >> {label}: {sender} | {subject}")
                    stats[classification] += 1
                else:
                    print(f"    FAIL moving {uid_str} to {dest}")
                    stats["errors"] += 1

        except Exception as e:
            print(f"    Error on UID {uid_str}: {e}")
            stats["errors"] += 1

    # Expunge deleted messages
    if not dry_run and (stats["autoreply"] > 0 or stats["application"] > 0):
        try:
            imap.expunge()
        except:
            pass

    return stats


