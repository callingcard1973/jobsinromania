def scan_account(imap, account_email, provider="a2", days=7):
    """List every email in INBOX with classification. Returns stats dict."""
    stats = {"total": 0, "autoreply": 0, "application": 0, "normal": 0, "errors": 0}

    try:
        status, data = imap.select("INBOX", readonly=True)
        if status != "OK":
            print(f"    Cannot select INBOX: {data}")
            return stats
    except Exception as e:
        print(f"    Cannot select INBOX: {e}")
        return stats

    since_date = (datetime.now() - timedelta(days=days)).strftime("%d-%b-%Y")
    try:
        status, data = imap.uid("SEARCH", None, f'(SINCE "{since_date}")')
        if status != "OK" or not data[0]:
            print(f"    No emails in last {days} days")
            return stats
    except Exception as e:
        print(f"    Search error: {e}")
        return stats

    uids = data[0].split()
    print(f"    {len(uids)} emails in last {days} days:")

    for uid in uids:
        try:
            status, header_data = imap.uid("FETCH", uid, "(BODY.PEEK[HEADER])")
            if status != "OK" or not header_data or not header_data[0]:
                stats["errors"] += 1
                continue

            raw = header_data[0][1] if isinstance(header_data[0], tuple) else header_data[0]
            if not isinstance(raw, bytes):
                stats["errors"] += 1
                continue

            msg = email.message_from_bytes(raw)
            sender = get_sender_email(msg)
            subject = decode_header_value(msg.get("Subject", ""))[:80]
            date_str = msg.get("Date", "")[:25]

            # Classify
            if is_autoreply(msg):
                tag = "AUTOREPLY"
                stats["autoreply"] += 1
            else:
                # Check body for application
                body_text = ""
                try:
                    s2, bd = imap.uid("FETCH", uid, "(BODY.PEEK[]<0.8000>)")
                    if s2 == "OK" and bd and bd[0]:
                        raw_body = bd[0][1] if isinstance(bd[0], tuple) else bd[0]
                        if isinstance(raw_body, bytes):
                            body_text = get_body_preview(raw_body)
                except:
                    pass

                if is_application(msg, body_text):
                    tag = "APPLICATION"
                    stats["application"] += 1
                else:
                    tag = "---"
                    stats["normal"] += 1

            stats["total"] += 1
            print(f"    [{tag:12s}] {date_str} | {sender:40s} | {subject}")

        except Exception as e:
            stats["errors"] += 1

    return stats


def run_scan(accounts, days=7):
    """Scan all accounts and list every email."""
    print("=" * 70)
    print(f"INBOX SCAN — {len(accounts)} accounts, last {days} days")
    print("=" * 70)

    totals = {"total": 0, "autoreply": 0, "application": 0, "normal": 0, "errors": 0}

    for acc in accounts:
        provider = acc["provider"]
        email_addr = acc["email"]
        print(f"\n  [{provider.upper()}] {email_addr}")

        imap, err = imap_connect(acc["imap_server"], acc["imap_port"], email_addr, acc["password"])
        if err:
            print(f"    FAILED: {err}")
            continue

        try:
            stats = scan_account(imap, email_addr, provider, days)
            for k in totals:
                totals[k] += stats[k]
        finally:
            try:
                imap.logout()
            except:
                pass

        time.sleep(0.3)

    print(f"\n{'=' * 70}")
    print(f"TOTALS: {totals['total']} emails — "
          f"{totals['autoreply']} autoreplies, {totals['application']} applications, "
          f"{totals['normal']} normal, {totals['errors']} errors")


