def make_ssl_context():
    ctx = ssl.create_default_context()
    ctx.check_hostname = False
    ctx.verify_mode = ssl.CERT_NONE
    return ctx


def imap_connect(server, port, email_addr, password):
    """Connect to IMAP and login. Returns (imap, None) or (None, error_str)."""
    try:
        imap = imaplib.IMAP4_SSL(server, port, ssl_context=make_ssl_context())
        imap.login(email_addr, password)
        return imap, None
    except Exception as e:
        return None, str(e)


def imap_list_folders(imap):
    """List all folder names."""
    status, data = imap.list()
    if status != "OK":
        return []
    folders = []
    for item in data:
        try:
            decoded = item.decode() if isinstance(item, bytes) else str(item)
            # Parse: (\flags) "delimiter" "name" or (\flags) "delimiter" name
            match = re.search(r'"([^"]*)"$|(\S+)$', decoded)
            if match:
                name = match.group(1) if match.group(1) is not None else match.group(2)
                folders.append(name)
        except:
            pass
    return folders


def imap_ensure_folders(imap, provider="a2"):
    """Create target folders if they don't exist. Returns list of created folders."""
    existing = imap_list_folders(imap)
    existing_lower = [f.lower() for f in existing]
    created = []

    for folder in TARGET_FOLDERS:
        # Check plain name
        if folder.lower() in existing_lower:
            continue
        # For A2, also check INBOX.prefix
        if provider == "a2" and f"inbox.{folder.lower()}" in existing_lower:
            continue

        # Try creating
        names_to_try = [folder]
        if provider == "a2":
            names_to_try.append(f"INBOX.{folder}")

        for name in names_to_try:
            try:
                status, _ = imap.create(name)
                if status == "OK":
                    created.append(name)
                    break
            except Exception as e:
                err = str(e).lower()
                if "already exists" in err or "alreadyexists" in err:
                    break
                continue

    return created


def imap_get_folder_name(imap, target, provider="a2"):
    """Find the actual folder name for a target (handles INBOX. prefix)."""
    existing = imap_list_folders(imap)
    for f in existing:
        if f.lower() == target.lower():
            return f
        if f.lower() == f"inbox.{target.lower()}":
            return f
    return target


def imap_move_email(imap, uid, dest_folder):
    """Move email by UID: COPY + DELETE. Returns True on success."""
    try:
        status, _ = imap.uid("COPY", uid, dest_folder)
        if status == "OK":
            imap.uid("STORE", uid, "+FLAGS", "\\Deleted")
            return True
        return False
    except:
        return False


