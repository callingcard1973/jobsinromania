def load_replied():
    """Load reply tracker from replied.json (shared lock for concurrent reads)."""
    try:
        f = open(REPLIED_JSON, "r")
    except FileNotFoundError:
        return {}
    try:
        if fcntl is not None:
            fcntl.flock(f.fileno(), fcntl.LOCK_SH)
        try:
            return json.load(f)
        except json.JSONDecodeError:
            return {}
    finally:
        if fcntl is not None:
            try:
                fcntl.flock(f.fileno(), fcntl.LOCK_UN)
            except OSError:
                pass
        f.close()


def save_replied(data):
    """Save reply tracker to replied.json (exclusive lock prevents concurrent overwrite)."""
    f = open(REPLIED_JSON, "w")
    try:
        if fcntl is not None:
            fcntl.flock(f.fileno(), fcntl.LOCK_EX)
        json.dump(data, f, indent=2)
        f.flush()
        os.fsync(f.fileno())
    finally:
        if fcntl is not None:
            try:
                fcntl.flock(f.fileno(), fcntl.LOCK_UN)
            except OSError:
                pass
        f.close()


def clean_replied(data, max_age_days=30):
    """Remove entries older than max_age_days."""
    cutoff = (datetime.now() - timedelta(days=max_age_days)).isoformat()
    cleaned = {}
    for account, senders in data.items():
        cleaned[account] = {
            s: ts for s, ts in senders.items() if ts > cutoff
        }
    return cleaned


def should_reply(replied_data, account_email, sender_email):
    """Check if we should send an auto-reply to this sender."""
    # Never reply to noreply/automated senders
    if NO_REPLY_SENDERS_RE.search(sender_email):
        return False

    # Never reply to our own accounts
    our_emails = set()
    for acc in A2_ACCOUNTS + GMAIL_ACCOUNTS + YAHOO_ACCOUNTS:
        our_emails.add(acc["email"].lower())
    if sender_email.lower() in our_emails:
        return False

    # Check interval
    account_data = replied_data.get(account_email, {})
    last_reply = account_data.get(sender_email.lower())
    if last_reply:
        try:
            last_dt = datetime.fromisoformat(last_reply)
            if datetime.now() - last_dt < timedelta(hours=AUTOREPLY_INTERVAL_HOURS):
                return False
        except:
            pass

    return True


def send_smtp_reply(smtp_server, smtp_port, from_email, password, to_email, original_subject):
    """Send auto-reply via SMTP. Returns True on success."""
    try:
        reply_subject = f"Re: {original_subject}" if original_subject else AUTOREPLY_SUBJECT

        msg = MIMEText(AUTOREPLY_BODY)
        msg["From"] = from_email
        msg["To"] = to_email
        msg["Subject"] = reply_subject
        msg["Auto-Submitted"] = "auto-replied"
        msg["X-Autoreply"] = "yes"
        msg["Precedence"] = "auto_reply"

        with smtplib.SMTP(smtp_server, smtp_port, timeout=30) as server:
            server.starttls(context=make_ssl_context())
            server.login(from_email, password)
            server.sendmail(from_email, to_email, msg.as_string())

        return True
    except Exception as e:
        print(f"    SMTP error to {to_email}: {e}")
        return False


def send_autoreplies_for_account(imap, account_email, password, smtp_server, smtp_port,
                                  days=7, dry_run=False):
    """Scan INBOX for emails that need auto-replies and send them."""
    replied_data = load_replied()
    stats = {"sent": 0, "skipped": 0, "errors": 0}

    try:
        imap.select("INBOX")
        since_date = (datetime.now() - timedelta(days=days)).strftime("%d-%b-%Y")
        status, data = imap.uid("SEARCH", None, f'(SINCE "{since_date}")')
        if status != "OK" or not data[0]:
            return stats
    except:
        return stats

    uids = data[0].split()

    for uid in uids:
        try:
            status, header_data = imap.uid("FETCH", uid, "(BODY.PEEK[HEADER])")
            if status != "OK" or not header_data or not header_data[0]:
                continue

            raw = header_data[0][1] if isinstance(header_data[0], tuple) else header_data[0]
            if not isinstance(raw, bytes):
                continue

            msg = email.message_from_bytes(raw)
            sender = get_sender_email(msg)
            subject = decode_header_value(msg.get("Subject", ""))

            # Don't reply to autoreplies or bounces
            if is_autoreply(msg):
                stats["skipped"] += 1
                continue

            if not should_reply(replied_data, account_email, sender):
                stats["skipped"] += 1
                continue

            if dry_run:
                print(f"    [DRY] Would reply to: {sender} | {subject[:60]}")
                stats["sent"] += 1
            else:
                if send_smtp_reply(smtp_server, smtp_port, account_email, password, sender, subject):
                    print(f"    Replied to: {sender}")
                    stats["sent"] += 1
                    # Track the reply
                    if account_email not in replied_data:
                        replied_data[account_email] = {}
                    replied_data[account_email][sender.lower()] = datetime.now().isoformat()
                else:
                    stats["errors"] += 1

        except Exception as e:
            stats["errors"] += 1

    if not dry_run and stats["sent"] > 0:
        replied_data = clean_replied(replied_data)
        save_replied(replied_data)

    return stats


